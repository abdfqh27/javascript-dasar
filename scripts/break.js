let counter = 1
while (true){
    document.writeln(`<p>Perulangan ke ${counter}</p>`);
    counter++;

    if (counter > 10){
        break; // Jika counter lebih besar sama dengan 10, maka perulangan akan dihentikan
    }   // Jika counter lebih besar sama dengan 10, maka perulangan akan dihentikan
}